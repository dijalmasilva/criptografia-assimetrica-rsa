    private void generateKeysButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateKeysButtonActionPerformed
        // TODO add your handling code here:

        String pText = pTextField.getText();
        BigInteger p = new BigInteger(pText);
        String qText = qTextField.getText();
        BigInteger q = new BigInteger(qText);

        assimetria.generateKeys(p, q);

        keyPublicText.setText(assimetria.getKeyPublicWithString());
        keyPrivateText.setText(assimetria.getKeyPrivateWithString());
    }//GEN-LAST:event_generateKeysButtonActionPerformed

    private void encryptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_encryptButtonActionPerformed
        // TODO add your handling code here:

        String message = messageText.getText();
        String encrypt = this.assimetria.encrypt(message);
        String decrypt = this.assimetria.decrypt(encrypt);
        messageEncryptText.setText(encrypt);
        messageDecryptText.setText(decrypt);

    }//GEN-LAST:event_encryptButtonActionPerformed

